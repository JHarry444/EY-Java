public class asdsfs {

    public static void main(String[] args) {
        int n = 1;
        while (n < 10) {
            int sum = 1;
            sum = sum * 2;
            System.out.println("(" + n + ", " + sum + ")");
            n++;
        }
        System.out.println("SUM: " + sum);
    }

}

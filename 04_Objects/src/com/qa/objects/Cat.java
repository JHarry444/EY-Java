package com.qa.objects;

// new type called 'Cat'
public class Cat {

    // has 3 'fields' or values
    String name;
    int age;
    String colour;

    public Cat() {
        // default constructor
        // function (or method) that is used to instantiate an object
    }

    public Cat(String name, int age, String colour) {
        this.name = name;
        this.age = age;
        this.colour = colour;
    }

}
